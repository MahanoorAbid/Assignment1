"use strict";
// Create an array of favorite fruits
const favorite_fruits = ['apples', 'bananas', 'strawberries'];
// Check for specific fruits using if statements
if (favorite_fruits.includes('bananas')) {
    console.log('You really like bananas!');
}
if (favorite_fruits.includes('apples')) {
    console.log('Apples are one of your favorite fruits!');
}
if (favorite_fruits.includes('strawberries')) {
    console.log('Strawberries are on your favorite fruits list!');
}
if (favorite_fruits.includes('oranges')) {
    console.log('You also like oranges!');
}
else {
    console.log('Oranges are not in your favorite fruits list.');
}
if (favorite_fruits.includes('grapes')) {
    console.log('You enjoy eating grapes too!');
}
else {
    console.log('Grapes are not among your favorite fruits.');
}
